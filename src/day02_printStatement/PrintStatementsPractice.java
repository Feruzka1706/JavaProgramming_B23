package day02_printStatement;

public class PrintStatementsPractice {

    public static void main(String[] args) {

        System.out.print("Hello"); //doesn't append new line
        System.out.print("Hola");//doesn't append new line

        System.out.println("How are you?");//appends new line after printing
        System.out.print("Today is Friday.");// doesn't append new line after printing
        System.out.println("Tomorrow is Saturday");

        System.out.println("--------------------------");
        System.out.println("\tHello Batch 23\n\t How are you today ");
        System.out.println("My name is \"Feruza\"");
        System.out.println("My name is 'Feruza'");

    }
}
/* \n-starting new line after this sequence
   \"- double quote \"your code\"
   \t-it represents paragraph space
   \\

 */