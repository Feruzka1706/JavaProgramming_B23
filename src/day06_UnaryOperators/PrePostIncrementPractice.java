package day06_UnaryOperators;

public class PrePostIncrementPractice {

    public static void main(String[] args) {
        int num1 = 100;
        int num2 = num1++;
        System.out.println(num1);
        System.out.println("------------------------");

        int P =100;
        System.out.println("P1 = " + P--);
        System.out.println("P2 = " + --P);
        System.out.println("P3 = " + --P);




    }
}
