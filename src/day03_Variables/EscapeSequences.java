package day03_Variables;

/*
\n: new line (Enter)
 */
public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello everyone \nHow are you all today?");

        System.out.println("------------------------");

        System.out.println("Hello everyone \nMy name is Feruza \nI live in california");

        System.out.println("------------------------");
        System.out.println("\tToday is a great day to study Java");

        System.out.println("My favorite TV series is \"Game of Throne\"");

    }

}
