package day04_Variables;

public class VariableNamingRules {

    public static void main(String[] args) {
        int num = 100;
        // double num = 200; variable name must be unique

        double a1 = 100;
        //double 2a = 100; variable name must start with letter
         String my_$name = "Muhtar";
         // variable name cannot have special characters other than _ and $
         String school = "Cybertek";
         double slary$ = 20000;
         // variable names starts with letters

        // char variable names cannot be Java reserved words

        boolean isFullTimeEmployee = true;
        // variable names have to be camelCase letters


    }
}
